def visa_investeringsforslag(df, valutakurser):
    st.subheader("üí° Investeringsf√∂rslag")
    kapital_sek = st.number_input("Tillg√§ngligt kapital (SEK)", value=500.0, step=100.0)

    riktkursval = st.selectbox("V√§lj riktkurs att j√§mf√∂ra mot", ["Riktkurs idag", "Riktkurs 2026", "Riktkurs 2027", "Riktkurs 2028"])
    endast_portfolj = st.checkbox("Visa endast innehav i portf√∂ljen")

    df = df.copy()
    df["Valutakurs"] = df["Valuta"].map(valutakurser).fillna(0.0)
    df["V√§rde (SEK)"] = df["Antal aktier"] * df["Aktuell kurs"] * df["Valutakurs"]

    df_port = df[df["Antal aktier"] > 0]
    portfoljvarde = df_port["V√§rde (SEK)"].sum()

    df["Potential (%)"] = ((df[riktkursval] - df["Aktuell kurs"]) / df["Aktuell kurs"]) * 100
    df = df[df["Potential (%)"] > 0].copy()

    if endast_portfolj:
        df = df[df["Antal aktier"] > 0].copy()

    df = df[df["Valutakurs"] > 0].sort_values(by="Potential (%)", ascending=False).reset_index(drop=True)

    if 'forslags_index' not in st.session_state:
        st.session_state.forslags_index = 0

    index = st.session_state.forslags_index
    if df.empty:
        st.info("Inga bolag matchar kriterierna.")
        return
    if index >= len(df):
        st.info("Inga fler f√∂rslag att visa.")
        return

    rad = df.iloc[index]
    if rad["Aktuell kurs"] <= 0:
        st.warning("Felaktig kurs.")
        return

    kapital_usd = kapital_sek / rad["Valutakurs"]
    antal = int(kapital_usd // rad["Aktuell kurs"])
    total_sek = antal * rad["Aktuell kurs"] * rad["Valutakurs"]
    andel_efter = round((rad["V√§rde (SEK)"] + total_sek) / (portfoljvarde + total_sek) * 100, 2)
    andel_nu = round(rad["V√§rde (SEK)"] / portfoljvarde * 100, 2) if portfoljvarde > 0 else 0.0

    st.markdown(f"""
        ### üí∞ F√∂rslag {index+1} av {len(df)}
        - **Bolag:** {rad['Bolagsnamn']} ({rad['Ticker']})
        - **Aktuell kurs:** {round(rad['Aktuell kurs'], 2)} {rad['Valuta']}
        - **{riktkursval}:** {round(rad[riktkursval], 2)} {rad['Valuta']}
        - **Potential:** {round(rad["Potential (%)"], 2)}%
        - **Antal att k√∂pa:** {antal} st
        - **Ber√§knad investering:** {round(total_sek, 2)} SEK
        - **Andel nuvarande portf√∂lj:** {andel_nu}%
        - **Andel efter k√∂p:** {andel_efter}%
    """)

    if st.button("‚û°Ô∏è N√§sta f√∂rslag"):
        st.session_state.forslags_index += 1


def visa_portfolj(df, valutakurser):
    st.subheader("üì¶ Min portf√∂lj")
    df = df[df["Antal aktier"] > 0].copy()
    if df.empty:
        st.info("Du √§ger inga aktier.")
        return

    df["Valutakurs"] = df["Valuta"].map(valutakurser).fillna(0.0)
    df["V√§rde (SEK)"] = df["Antal aktier"] * df["Aktuell kurs"] * df["Valutakurs"]
    df["Andel (%)"] = round(df["V√§rde (SEK)"] / df["V√§rde (SEK)"].sum() * 100, 2)
    df["√Örlig utdelning (SEK)"] = df["√Örlig utdelning"] * df["Antal aktier"] * df["Valutakurs"]

    total_utdelning = df["√Örlig utdelning (SEK)"].sum()
    snitt_per_manad = total_utdelning / 12

    st.metric("Total portf√∂ljv√§rde", f"{int(df['V√§rde (SEK)'].sum()):,} SEK")
    st.metric("F√∂rv√§ntad √•rlig utdelning", f"{round(total_utdelning, 2):,} SEK")
    st.metric("Snittutdelning per m√•nad", f"{round(snitt_per_manad, 2):,} SEK")

    st.dataframe(df[[
        "Bolagsnamn", "Ticker", "Antal aktier", "Aktuell kurs", "Valuta",
        "V√§rde (SEK)", "Andel (%)", "√Örlig utdelning", "√Örlig utdelning (SEK)"
    ]], use_container_width=True)

